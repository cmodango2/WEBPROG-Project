                <p>The DRRH capitalizes on the basic need of hotels to promote and market itself to prospective guests. It allows hotels to market their properties, facilities, and services from a different perspective — its ability to bounce back better from disasters and crises.</p>
                <br>
                <p>Following the framework, the rating looks into a hotel’s policies, programs, tools, systems, and training that will allow it to prevent, mitigate, prepare for, and respond to disasters.</p>
                <br>
                <p>Through the DRRH, these elements of operations that are not usually known to the public are evaluated and packaged such that potential guests are made aware of the hotel’s ability to ensure their safety and well-being before, during, and after any disaster or crisis. Thus, the DRRH strengthens the hotel’s reputation while reinforcing resilience consciousness among its stakeholders, most especially the hotel-going public.</p>
                </br>
            

