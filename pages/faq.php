      <button type="button" class="collapsible">What is the Disaster Resilience Rating for Hotels?</button>
          <div class="content">
              <p><br>The DRRH is a thesis (capstone) project by a group of graduate students from the Asian Institute of Management (AIM), centered on co-creating a disaster resilience rating together with hotel industry associations/groups that will be shared publicly via Online Travel Agents (OTAs) and other media platforms as a way of gaining confidence from the general public.</p.>
          </div>
        </div>
  
    <div class="column">
      <button type="button" class="collapsible">What are the procedures in having a hotel rated?</button>
          <div class="content"><br>
              After registration, the hotel will be guided on the process of self-assessment, the natural starting point of the evaluation.

              Once the self-assessment is completed and the documentary requirements have been submitted, a third party auditor will coordinate with the hotel for the actual field audit where evaluators will visit the hotel and validate the self-assessment.

              The auditor will then submit the findings to the industry association who will serve as the final approver of the rating.

              Once approved, the hotel will be allowed to use the rating in all its promotional activities.
          </div>
        </div>
      </div>
    </div>
      
