      <button type="button" class="collapsible">How much will the rating cost?</button>
          <div class="content">
              <p><br>The cost that will be charged to the hotel is the conduct of the third-party audit.
              Initial estimates show that this may run anywhere between Php50 - 75K excluding travel and accommodation expenses for field audit. </p.>
          </div>
        </div>
  
    <div class="column">
      <button type="button" class="collapsible">Can the hotel suddenly back-out during the conduct of the rating? Will the payment be refunded?</button>
          <div class="content"><br>
          The rating will only commence after payment of audit fees and completion of the self-assessment.

There is a time for completing the self-assessment as well as time period for the field audit and the final approval.

Should a hotel wish to discontinue the rating process, after the self-assessment, it will have to formally advise the industry association, but the fee will not be refunded when the field audit has been conducted.
          </div>
        </div>
