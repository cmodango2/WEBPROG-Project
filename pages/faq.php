     <div class="row">
      
      <div class="column">
      <button type="button" class="collapsible">How will the rating measure the hotel's level of disaster resilience?</button>
          <div class="content">
              <p><br>The DRRH follows the Resilience Programming Framework (RPF) created by Resilient.PH. The rating will evaluate the management practices — Asset Protection and Risk Management (APRM), Business Continuity Management (BCM), Disaster Risk Reduction (DRR), 
              Emergency, Crisis and Opportunity Management (ECOM) and Risk/Crisis Communication (R/CCOMM) by reviewing existing policies, programs, systems, trainings, and tools (including finances) of hotels and assigning scores based on resilient level criteria - 
              base (for local industry practices/compliance driven practices) mid (international best practice/above compliance) and high (for proven effective novel practices/thought leadership).</p.>
          </div>
        </div>
  
    <div class="column">
      <button type="button" class="collapsible">What does the hotel need to provide/submit to be rated?</button>
          <div class="content"><br>
              As the critical areas for review includes policies, programs, systems, training, and tools (including finances), the rating will require hotels to show proof of the elements of the RPF in these areas.

              Description of training programs for safety, company policy on disaster risk reduction, photos of health and safety features and resources, etc. are some of the examples of documents that will be asked of hotels undergoing the rating.
          </div>
        </div>
      </div>
    </div>
      
    <!-- End of second row -->

    <!-- Start of third row -->
    <div class="row">
      
      <div class="column">
      <button type="button" class="collapsible">How will the hotel benefit from the rating?</button>
          <div class="content">
              <p><br>With the DRRH, hotels will be able to identify vulnerable components of their operations which they can work on to build their overall resilience.
              More importantly, the rating will show the hotel’s  commitment to ensuring safety and wellbeing of guests and employees before, during, and after any hazard event which will add to the positive perception/image of the hotel.
              The survey we conducted among hotel goers show that guests are likely to choose a hotel that they perceive to be resilient.</p.>
          </div>
        </div>
  
    <div class="column">
      <button type="button" class="collapsible">What will the auditors/inspectors do when conducting the rating?</button>
          <div class="content"><br>
              The third-party audit process has two components — the first part includes checking the completeness and validity of documents submitted by the hotel being audited. The self-assessment results will be reviewed following the criteria set by the DRRH.

              The second part involves field auditors visiting the hotel and validating the documents in the self-assessment through actual inspection, interview with employees and guests, interview with local government agencies and the community.
          </div>
        </div>
      </div>
    </div>
      
