    <div class="column">
      <button type="button" class="collapsible">How long is the whole rating process?</button>
          <div class="content"><p><br>The self-assessment is to be completed within one month. The first part of the third-party audit will begin as soon as the self-assessment has been submitted and will run for a maximum of one week.

The field audit will be done after the desk review has been completed and is expected to last anywhere between 3 to 5 days. The findings of the third-party audit will be transmitted to the hotel industry association who will be given a maximum of two weeks to approve or reject the rating.

If there are no questions about the audit findings, the final approval will be communicated two weeks after submission of the third-party audit findings.</p>
          
