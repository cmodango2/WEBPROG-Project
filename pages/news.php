                <p><strong>Now that restrictions are slowly being lifted, and some semblance of normalcy is beginning to come back, what should hotel owners and management do to win back guests while also preparing for the next global disaster?</strong></p>
                <br>
                <p>This COVID-19 pandemic is an unprecedented event that has led to massive unemployment and economic downturns worldwide. The hotel sector, which is highly dependent on tourists and travelers, has been severely affected. Now that restrictions are slowly being lifted, and some semblance of normalcy is beginning to come back, what should hotel owners and management do to win back guests while also preparing for the next global disaster? What can hotels do to become more disaster resilient?</p>
                <br>
                <p>A group of graduate students of the Executive Master in Disaster Risk and Crisis Management (AIM), studying disaster risks and crisis management from one of the country’s leading learning institutions, is taking on the challenge of driving resilience consciousness among hotelgoers.</p>
                </br>
        

            <div class="description-news">
                <img src="../../img/new3.jpg" class="img-resp float-img right">
                <br>
                <p>Their thesis project — the <strong>Disaster Resilience Rating for Hotels (DRRH)</strong>, is specifically designed to enable the accommodations industry to bounce back better after every disaster.</p>
                <br>
                <p>According to the group leader, <strong>Randell Kitt Botero</strong>, <em>“In today’s volatile, uncertain, complex, and ambiguous world, resilience has become a buzz word. While usually associated with disasters and crises, the term is now widely used across disciplines. That said, it has also come to mean different things to different people. Some equate resilience with preparedness. Others associate it with being able to respond and effectively manage hazard events. In the broader interpretation of the word, it means the ability to bounce back after a shock. But what does disaster resilience mean for the hotel industry? This is at the core of our thesis.”</strong></em></p>
                <br>
                <p>The group’s thesis project is anchored on the Resilience Programming Framework (RPF) developed by Resilient.PH. The framework integrates already existing principles Into the organization’s very DNA, thus reframing traditional approaches on the need to effectively and sustainably bounce back better.</p>
